package com.apiverve.stockexchange.data;

import com.fasterxml.jackson.annotation.*;
import java.time.LocalDate;

public class StockExchangeData {
    private String mic;
    private String operatingMic;
    private String type;
    private String name;
    private String acronym;
    private String category;
    private String country;
    private String city;
    private String website;
    private String lei;
    private LocalDate creationDate;

    @JsonProperty("mic")
    public String getMic() { return mic; }
    @JsonProperty("mic")
    public void setMic(String value) { this.mic = value; }

    @JsonProperty("operatingMic")
    public String getOperatingMic() { return operatingMic; }
    @JsonProperty("operatingMic")
    public void setOperatingMic(String value) { this.operatingMic = value; }

    @JsonProperty("type")
    public String getType() { return type; }
    @JsonProperty("type")
    public void setType(String value) { this.type = value; }

    @JsonProperty("name")
    public String getName() { return name; }
    @JsonProperty("name")
    public void setName(String value) { this.name = value; }

    @JsonProperty("acronym")
    public String getAcronym() { return acronym; }
    @JsonProperty("acronym")
    public void setAcronym(String value) { this.acronym = value; }

    @JsonProperty("category")
    public String getCategory() { return category; }
    @JsonProperty("category")
    public void setCategory(String value) { this.category = value; }

    @JsonProperty("country")
    public String getCountry() { return country; }
    @JsonProperty("country")
    public void setCountry(String value) { this.country = value; }

    @JsonProperty("city")
    public String getCity() { return city; }
    @JsonProperty("city")
    public void setCity(String value) { this.city = value; }

    @JsonProperty("website")
    public String getWebsite() { return website; }
    @JsonProperty("website")
    public void setWebsite(String value) { this.website = value; }

    @JsonProperty("lei")
    public String getLei() { return lei; }
    @JsonProperty("lei")
    public void setLei(String value) { this.lei = value; }

    @JsonProperty("creationDate")
    public LocalDate getCreationDate() { return creationDate; }
    @JsonProperty("creationDate")
    public void setCreationDate(LocalDate value) { this.creationDate = value; }
}